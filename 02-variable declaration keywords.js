// var,let,const

//var
//can be redeclared and updated
var name = "Rahul";
var name = "Raju";
console.log(name);

//let
//can't redeclare but can update
let _name= "Rahul";
_name = "Raju";
console.log(_name);

//const
//can't redeclare or update
const fullName = "Rahul Melkani"
console.log(fullName);
